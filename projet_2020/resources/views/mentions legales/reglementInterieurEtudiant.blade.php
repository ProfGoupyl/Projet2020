<h1>Règlement Intérieur Étudiant</h1>
<h2>Article 1. Objet et champ d'application du règlement</h2>
<p>
    Le présent règlement s'applique à tous les Étudiants, qui suivent un parcours de formation sur la plateforme <strong>Goupyl Education</strong> (les « Étudiants »).
    Le règlement intérieur vise à réguler les rapports entre les membres de la communauté éducative de <strong>Goupyl Education</strong> afin de permettre les meilleures 
    conditions de travail à l'ensemble des étudiants, propices à la réussite de leur formation, et ce, en prenant en compte les particularités de
    l'enseignement dispensé essentiellement à distance, en ligne sur la plateforme <strong>Goupyl Education</strong>. 
</p>
<h2>Article 2. Règles de fonctionnement</h2>
<h3>2.1 Principes essentiels</h3>
<p>
    Les espaces éducatifs de <strong>Goupyl Education</strong> reposent sur les valeurs et les principes suivants, dont le respect s'imposent à tous, de tolérance, d'entraide,
    de bienveillance, de courtoisie, de civilité et de savoir-vivre ensemble, de respect d'autrui dans sa personne et ses convictions, à la protection contre toute
    forme de violence verbale, morale, psychologique, ou physique.
    L'usage de la violence, sous quelque forme que ce soit, est interdit et ne sera pas toléré au sein des espaces en ligne ou hors ligne de <strong>Goupyl Education</strong>.
    Les Étudiants sont ainsi invités à participer aux différents espaces communautaires de <strong>Goupyl Education</strong> (dont les forums), en respectant les valeurs et principes
    évoqués ci-avant ainsi que la législation en vigueur.
</p>
<h3>2.2 Organisation des formations</h3>
<p>
    Chaque Parcours comporte un certain nombre de cours que l'Étudiant doit valider.
    Pour valider sa formation, l'Étudiant doit :
</p>
<ul>
    <li>
        Réaliser la totalité des cours composant sa formation et les faire valider,
        une première fois, par son mentor.
    </li>
    <li>
        Lorsque le projet l'exige, valider auprès du mentor validateur, la totalité des compétences indiquées dans les cours lors d'une soutenance ultérieure, réalisée
        en visioconférence. Cette soutenance est enregistrée et sauvegardée dans le dossier de l'Étudiant.
    </li>
    <li>
        Soumettre et faire valider son portfolio auprès du jury du titre. La décision des membres du jury est souveraine.
    </li>
</ul>
<h3>2.3 Modalités Jury</h3>
<h4>2.3.1 Composition du jury</h4>
<p>
    Le jury est composé de professionnels du secteur et de représentants de <strong>Goupyl Education</strong>. Il se réunit, physiquement ou à distance, de façon synchrone ou asynchrone,
    pour délibérer au vu des dossiers de chaque candidat au diplôme. Les dossiers de chaque candidat sont composés des éléments suivants :
</p>
<ul>
    <li>
        Une présentation du profil de l'Étudiant ;
    </li>
    <li>
        L'ensemble des compte-rendus de suivi rédigés par les mentors sur la progression et l'assiduité de l'Étudiant ;
    </li>
    <li>
        Tous les projets de l'Étudiant composés de : soutenances vidéos enregistrées, les livrables réalisés et les
        commentaires du mentor ayant validé la soutenance.
    </li>
</ul>
<h4>2.3.2 Rattrapage</h4>
<p>
    Lorsque le jury refuse l'obtention du diplôme, il propose des axes d'amélioration pour soumettre, sous 6 mois, un ou plusieurs nouveaux projets axés sur les
    compétences à renforcer. Ces projets seront validés par les membres du jury.
</p>
<h3>2.4 Equipement nécessaire</h3>
<p>
    Les enseignements de <strong>Goupyl Education</strong> étant dispensés en ligne, chaque Étudiant doit obligatoirement avoir accès à un réseau internet de qualité
    suffisante permettant d'effectuer des visioconférences sans interruptions, avoir en sa possession une webcam ainsi qu'un micro permettant de réaliser les sessions
    hebdomadaires de mentorat (art. 3.2) dans des conditions optimales.
    Un aménagement est prévu pour les Étudiants en situation de handicap.
</p>
<h3>2.5 Modalités de l'accompagnement pédagogique Mentorat</h3>
<p>
    Ces sessions de mentorat se déroulent exclusivement par le biais d'une visio-conférence, impliquant pour l'Étudiant l'obligation pour chaque
    session et soutenance d'avoir en sa possession une webcam et un micro opérationnels. Pour les Étudiants justifiant d'une situation de handicap,
    une soutenance pourra être organisée en audio-conférence.
    L'Étudiant accepte que ses sessions de mentorat puissent être filmées à des fins de qualité exclusivement internes et propres à <strong>Goupyl Education</strong>.
    Les Étudiants doivent appliquer les règles de ponctualité, civilité, et se présenter dans un environnement direct adapté, un langage et avec une
    tenue vestimentaire corrects à toutes les sessions de mentorat ainsi qu'aux soutenances.
</p>
<h3>2.6 Modalités de contrôle continu des connaissances</h3>
<p>
    Les sessions de soutenance sont enregistrées et conservées pendant toute la durée de la formation dans l'objectif de servir comme moyen de contrôle
    continu de la formation. Elles seront présentées et visionnées par les membres du jury final de diplomation. Elles constituent le dossier académique de l'Étudiant.
</p>
<h3>2.7 Arrêt et suspension des formations</h3>
<p>
    En cas d'arrêt anticipé de la formation ou de suspension de la formation, l'Étudiant doit en notifier son Mentor.
    Dans le cas d'une suspension longue (d'une durée supérieure à 15 jours continus), l'Étudiant doit également en faire mention au Service Scolarité (Student Success), via l'email : <strong>students@goupyl.com </strong>
    Les suspensions d'une durée d'une semaine (soit une session de mentorat) sont autorisées et reportables, sous réserve que le Mentor en ait été informé au préalable et sous réserve de sa disponibilité.
    Les Étudiants financés par un organisme de financement tel que <strong>(Le Forem, Opco, Transitions pro, ou autres)</strong> n'ont pas la possibilité de faire de pause sans en référer préalablement à leur organisme
    de financement. Un accord écrit officiel de l'organisme de financement sera obligatoire pour que <strong>Goupyl Education</strong> prenne en considération ladite pause et que le report de la durée équivalente à la période
    de suspension en fin de la formation soit effectif.
</p>
<h3>2.8 Demande de changement de formation</h3>
<p>
    Toute demande de changement de parcours doit être préalablement soumise à <strong>Goupyl Education</strong> via un email à <strong>students@goupyl.com</strong>, après consultation obligatoire du mentor. Le changement de parcours demande au minimum
    7 jours ouvrés de traitement.
    Les Étudiants qui sont financés par un organisme financeur tiers doivent au préalable faire une demande de changement de parcours auprès de leur organisme financeur. Le changement de parcours ne pourra être effectif
    sans accord écrit officiel de l'organisme.
</p>
<h2>Article 3. Obligations des Étudiants</h2>
<h3>3.1 Assiduité</h3>
<p>L'Étudiant est tenu à une obligation générale d'assiduité.</p>
<h3>3.2 Participation obligatoire aux séances de mentorat des Parcours</h3>
<p>
    L'Étudiant doit être présent à toutes les sessions de mentorat hebdomadaires.
    En cas d'absence à une session de mentorat :
</p>
<ul>
    <li>
        L'Étudiant doit impérativement prévenir son mentor plus de 24h à l'avance,
        en justifiant son absence afin de pouvoir reporter la session, si possible dans la même semaine
        ou la semaine suivante. Ce report de session peut aboutir à programmer 2 sessions de mentorat dans une même semaine, ce à quoi l'Étudiant devra se conformer.
    </li>
    <li>
        Si l'Étudiant ne prévient pas ou prévient moins de 24h avant la session programmée, celle-ci sera considérée comme une absence non-justifiée et ne sera pas reportée, sauf cas exceptionnel
        soumis à l'autorisation préalable du Service Scolarité de <strong>Goupyl Education</strong>. Est considérée comme une “absence non autorisée”, toute session annulée par l'Étudiant moins de 24 (vingt-quatre)
        heures avant la session ou un retard de plus de 15 (quinze) minutes lors d'une session.
    </li>
</ul>
<p>
    Dans les 2 cas cités ci-dessus, l'Étudiant doit envoyer un email au mentor afin de le tenir informé des raisons de l'absence (avant et/ou après la session programmée) et, le cas échéant,
    re-programmer une session avec le Mentor dans les sept (7) jours de l'absence.
</p>
<p>
    Après trois absences non autorisées consécutives de l'Étudiant, le Conseil de discipline pourra prononcer une sanction pouvant aller jusqu'à l'exclusion de celui-ci de la formation.
</p>
<h3>3.3 Implication</h3>
<p>
    En vue du succès de leur formation, les Étudiants se doivent de fournir un travail régulier tout au long de leur formation, dont la réalisation des projets qu'ils devront soumettre
    à leur Mentor lors des sessions hebdomadaires.
    L'Étudiant devra aussi se soumettre au contrôle des connaissances lors des sessions de soutenance.
    En cas de non-respect des dispositions du présent article le Conseil de discipline pourra prendre des sanctions disciplinaires à l'encontre de l'Étudiant.
</p>
<h3>3.4 Respect de la propriété intellectuelle</h3>
<p>
    L'ensemble des contenus accessibles sur la plateforme est protégé par la réglementation régissant les droits d'auteur et ne peut être utilisée autrement que dans les conditions de
    la licence <strong>(Creative Commons « CC BY-NC-SA 4.0 »)</strong> dite « Attribution - Pas d'Utilisation Commerciale - Partage dans les mêmes Conditions 4.0 »
</p>
<p>
    Il est formellement interdit de recopier intégralement ou partiellement un texte, des photos, le travail d'autrui sans faire référence à la source d'origine.
</p>
<p>
    Si une reproduction illégale devait être constatée, le Conseil de discipline se saisira automatiquement du dossier et appliquera une sanction pouvant
    aller jusqu'à l'exclusion de la formation.
</p>
<p>
    De même, si un cas de triche ou de reproduction du travail d'un autre Étudiant était avéré, le Conseil de discipline se saisira automatiquement du dossier et
    appliquera une sanction pouvant aller jusqu'à l'exclusion de la formation.
</p>
<h3>3.5 Interdiction de la violence et respect d'autrui</h3>
<p>
    Tout comportement jugé injurieux, calomnieux, xénophobe, raciste, diffamatoire, obscène, sexiste, homophobe, menaçant, illégal ou plus généralement considéré
    comme inacceptable par <strong>Goupyl Education</strong> entraînera immédiatement une procédure disciplinaire et une possible exclusion de la formation suite à la décision du Conseil de discipline et éventuellement une procédure pénale.
</p>
<h2>Article 4. Discipline</h2>
<h3>4.1 Conseil de discipline</h3>
<strong>Composition</strong>
<p>Chaque Conseil de discipline pourra être constitué des personnes suivantes :</p>
<ul>
    <li>Directeur·rice pédagogique, président du Conseil de discipline</li>
    <li>Responsable pédagogique du parcours/ de la formation</li>
    <li>Un représentant étudiant (à titre consultatif et sur invitation de l'Étudiant)</li>
</ul>
<p>
    Tout Conseil de discipline se tiendra à distance, par système de visioconférence. Les convocations sont adressées par e-mail aux Étudiants au moins 3 jours avant la tenue de la séance du conseil.
</p>
<strong>Décisions du Conseil de discipline</strong>
<p>
    La décision du Conseil de discipline sera communiquée par lettre recommandée avec accusé réception à l'Étudiant au plus tôt dans les deux jours suivant le conseil et au plus tard dans les 15 jours après l'entretien.
    La décision du Conseil de discipline n'est pas susceptible d'appel.
</p>
<p>
    Toute décision disciplinaire du Conseil sera également transmise aux organismes officiels <strong>(Le Forem, Opco...)</strong> dans le cas d'une prise en charge de la formation, qu'elle soit partielle ou totale et à l'employeur en cas
    d'action de formation dans le cadre du plan de formation d'une entreprise.
</p>
<h3>4.2 Procédure et mesures disciplinaires</h3>
<p>
    Tout manquement de l'Étudiant à l'une des obligations du présent règlement intérieur ou bien s'il est auteur ou complice d'une fraude ou tentative de fraude commise à l'occasion d'une soutenance pourra
    faire l'objet d'une procédure.
</p>
<p>
    Aucune sanction ne peut être infligée à un Étudiant sans que celui-ci ait été informé au préalable des griefs retenus contre lui.
</p>
<p>
    Tout Étudiant convoqué en Conseil de discipline a le droit de se faire assister par un représentant étudiant qui se trouve dans le même parcours de formation. Cette mention figure impérativement sur les convocations
    ainsi que la date, l'heure et le lieu de l'entretien.
</p>
<p>
    Pour le cas où l'Étudiant ou son représentant serait absent à la convocation du Conseil de discipline, ce dernier se tiendrait tout de même, sauf en cas de force majeure (accident, maladie, décès d'un proche de descendance
    ou d'ascendance directe) qui devrait être prouvé par un document officiel (justificatif d'hospitalisation, arrêt maladie, faire-part de décès).
</p>
<p>
    Lorsque le/la Directeur·rice Pédagogique ou son représentant envisage de prendre une sanction, qui a une incidence, immédiate ou non, sur la présence d'un Étudiant dans une formation, il peut convoquer une réunion
    du Conseil de discipline.
</p>
<p>
    Le Conseil de discipline convoque l'Étudiant en lui indiquant l'objet de cette convocation. Celle-ci précise la date, l'heure et le lieu de l'entretien. Elle est écrite et est adressée par email ou remise
    à l'intéressé contre décharge.
</p>
<p>
    Le Conseil de discipline indique le motif de la sanction envisagée et recueille les explications de l'Étudiant.
</p>
<p>
    Tout agissement considéré comme fautif pourra, en fonction de sa nature et de sa gravité, faire l'objet de l'une ou l'autre des sanctions suivantes :
</p>
<ol>
    <li>Avertissement</li>
    <ul>
        <li>Information par email.</li>
    </ul>
    <li>Suspension</li>
    <ul>
        <li>Exclusion temporaire de la formation d’une durée de 1 mois.</li>
        <li>
            Le mois d’exclusion est dû financièrement quel que soit le dispositif
            de financement de l’Étudiant (personnel ou par un tiers financeur).
        </li>
    </ul>
    <li>Exclusion définitive</li>
    <ul>
        <li>Arrêt immédiat de la formation.</li>
        <li>Les frais engagés ne sont pas remboursés</li>
        <li>Interruption de l’accès aux différents outils collaboratifs, ainsi qu’au mentorat.</li>
        <li>Exclusion de toute formation <strong>Goupyl Education</strong> pendant 1 an.</li>
    </ul>
</ol>
<p>
    Quel que soit le motif de la sanction, celle-ci sera inscrite dans le dossier de l’Étudiant et sera prise
    en compte lors du Jury final de diplômation
</p>
<h2>Article 5. Règles d'hygiène et de sécurité</h2>
<p>
    Dans le cas où une partie de la formation suivie par les Étudiants se déroulerait hors de la plateforme,
    dans un établissement physique ou une entreprise dotée d'un règlement intérieur conformément à la loi du 4 août 1982,
    les mesures d'hygiène et sécurité applicables aux Étudiants seront celles dudit règlement, auquel les Étudiants devront se conformer.
</p>